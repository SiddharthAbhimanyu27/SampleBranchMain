package Programs;

public class Floyds_Triangle 
{
	public static void main(String[] args) 
	{
		for(int i=0;i<8;i++)
		{
			for(int j=0;j<=i;j++) //so that starts are equal to rows and  columns number 
			{
				System.out.print("*");
			}
			
			System.out.println();// to Print in the next line
			
		}
		
	}

}
